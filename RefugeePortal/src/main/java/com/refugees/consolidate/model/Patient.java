package com.refugees.consolidate.model;


public class Patient {
    private String ifa_date;
    private String patient_id;
    private String full_name;
    private String sex;
    private String birth_day;
    private String height;
    private String weight;
    private String blood_abo;
    private String systolic;
    private String diastolic;
    private String smoking;
    private String insurer;

    public String getIfa_date() {
        return ifa_date;
    }

    public void setIfa_date(String ifa_date) {
        this.ifa_date = ifa_date;
    }

    public String getPatient_id() {
        return patient_id;
    }

    public void setPatient_id(String patient_id) {
        this.patient_id = patient_id;
    }

    public String getFull_name() {
        return full_name;
    }

    public void setFull_name(String full_name) {
        this.full_name = full_name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getBirth_day() {
        return birth_day;
    }

    public void setBirth_day(String birth_day) {
        this.birth_day = birth_day;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBlood_abo() {
        return blood_abo;
    }

    public void setBlood_abo(String blood_abo) {
        this.blood_abo = blood_abo;
    }

    public String getSystolic() {
        return systolic;
    }

    public void setSystolic(String systolic) {
        this.systolic = systolic;
    }

    public String getDiastolic() {
        return diastolic;
    }

    public void setDiastolic(String diastolic) {
        this.diastolic = diastolic;
    }

    public String getSmoking() {
        return smoking;
    }

    public void setSmoking(String smoking) {
        this.smoking = smoking;
    }

    public String getInsurer() {
        return insurer;
    }

    public void setInsurer(String insurer) {
        this.insurer = insurer;
    }


}
